#include "../ft_printf.h"

int	ft_print_str(char *s)
{
	int i;

	if (!s)
        s = "(null)";
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}